$(document).ready(function(){
	

$("#connect").click(function() {
	$("#modal-container").show()
	});


$("#button").click(function() {
	$("#modal-container").hide()
	});

$("#modal-overlay").click(function() {
	$("#modal-container").hide()
	});

$("#image").click(function() {
	window.location.href="about.html"
	});


});